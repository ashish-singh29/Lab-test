package labtest;

public class OddNumbers {

	public static void main(String[] args) {
		OddNumbers();
		
	}
	  public static void OddNumbers() {
	        {

	            int number = 20;
	            System.out.println("List of odd numbers from 1 to 20 ::");
			//logic for Odd Numbers Between 1 to 20.
	            for (int i = 1; i <= number; i++) {

	                if (i % 2 != 0) {
	                    System.out.print(i + " ");
	                }
	            }

	        }
	  }
	 
}